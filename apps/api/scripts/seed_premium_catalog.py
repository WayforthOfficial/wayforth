def normalize_db_url(url: str) -> str:
    if url.startswith("postgres://"):
        return "postgresql://" + url[len("postgres://"):]
    return url


async def probe_services(services: list[dict], timeout: float = 5.0) -> dict[str, bool]:
    """HEAD-probe up to 100 randomly sampled services; return {endpoint_url: reachable}."""
    sample = random.sample(services, min(100, len(services)))
    results: dict[str, bool] = {}

    async def _head(client: httpx.AsyncClient, url: str) -> tuple[str, bool]:
        try:
            r = await client.head(url, timeout=timeout, follow_redirects=True)
            return url, r.status_code < 500
        except Exception:
            return url, False

    async with httpx.AsyncClient() as client:
        tasks = [_head(client, s["endpoint_url"]) for s in sample]
        for coro in asyncio.as_completed(tasks):
            url, ok = await coro
            results[url] = ok

    return results


